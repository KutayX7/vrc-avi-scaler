# DEPRECATED
def reset_config_file() -> None:
    config = globals.config
    config.reset()
    config.save()

# DEPRECATED
def save_config() -> None:
    print(_("config.saving"))
    config = globals.config
    result = config.save()
    if result == ConfigSaveResult.SUCCESS:
        print(_("config.saved"))
    else:
        print(f"Error: {result}")

# DEPRECATED
def read_config() -> ConfigLoadResult:
    config: Config = globals.config
    status = config.load()
    if status != ConfigLoadResult.SUCCESS:
        return status

    globals.FPS = config.get('fps')
    globals.smooth_scaling_step_frequency = globals.FPS * 4.0
    globals.save_config_on_exit = config.get('autosave')

    globals.osc_client_ip = config.get('osc.client.ip')
    globals.osc_client_port = config.get('osc.client.port')
    globals.osc_server_ip = config.get('osc.server.ip')
    globals.osc_server_port = config.get('osc.server.port')
    globals.oscquery_enabled = config.get('oscquery.enable')

    globals.compat_mags = config.get("compat.Mag's Scale Adjuster")
    globals.compat_jackal = config.get("compat.Jackal Scaling System")
    globals.compat_openvrcs = config.get("compat.OpenVRCScaler")

    globals.preferred_locale = config.get("localisation.locale")

    translator.set_locale(globals.preferred_locale)
    if globals.translator and globals.translator != translator:
        globals.translator.set_locale(globals.preferred_locale)

    return ConfigLoadResult.SUCCESS
